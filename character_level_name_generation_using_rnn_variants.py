#  CHARACTER VOCABULARY


class CharVocab:
    """Map characters ↔ indices. Includes START and END tokens."""

    START = "^"
    END   = "$"
    PAD   = " "

    def __init__(self, names: list[str]):
        chars = sorted(set("".join(names)) | {self.START, self.END, self.PAD})
        self.char2idx = {c: i for i, c in enumerate(chars)}
        self.idx2char = {i: c for c, i in self.char2idx.items()}
        self.size = len(chars)
        self.start_idx = self.char2idx[self.START]
        self.end_idx   = self.char2idx[self.END]
        self.pad_idx   = self.char2idx[self.PAD]

    def encode(self, name: str) -> list[int]:
        return [self.char2idx[c] for c in (self.START + name + self.END)
                if c in self.char2idx]

    def decode(self, indices: list[int]) -> str:
        return "".join(self.idx2char.get(i, "?") for i in indices
                       if i != self.start_idx and i != self.pad_idx)

    def one_hot(self, idx: int) -> np.ndarray:
        v = np.zeros(self.size)
        v[idx] = 1.0
        return v



#  ACTIVATION FUNCTIONS


def sigmoid(x):
    return 1.0 / (1.0 + np.exp(-np.clip(x, -50, 50)))

def tanh(x):
    return np.tanh(np.clip(x, -50, 50))

def softmax(x):
    e = np.exp(x - x.max())
    return e / e.sum()



#  MODEL 1 – VANILLA RNN


class VanillaRNN:
    """
    Architecture:
      h_t = tanh(W_xh · x_t  +  W_hh · h_{t-1}  +  b_h)
      y_t = W_hy · h_t  +  b_y

    Trained with truncated BPTT.
    """

    def __init__(self, vocab: CharVocab, hidden_size: int = 128):
        self.vocab = vocab
        self.hidden_size = hidden_size
        V = vocab.size
        H = hidden_size

        # Xavier init
        self.W_xh = np.random.randn(H, V)  * np.sqrt(2 / (V + H))
        self.W_hh = np.random.randn(H, H)  * np.sqrt(2 / (H + H))
        self.b_h  = np.zeros((H, 1))
        self.W_hy = np.random.randn(V, H)  * np.sqrt(2 / (H + V))
        self.b_y  = np.zeros((V, 1))

        # Adam optimiser state
        self.params = [self.W_xh, self.W_hh, self.b_h, self.W_hy, self.b_y]
        self._init_adam()

    def _init_adam(self):
        self.m = [np.zeros_like(p) for p in self.params]
        self.v = [np.zeros_like(p) for p in self.params]
        self.t = 0

    @property
    def name(self):
        return "VanillaRNN"

    def num_params(self) -> int:
        return sum(p.size for p in self.params)

    def forward(self, inputs: list[int], h_prev: np.ndarray):
        """
        inputs : list of char indices  (length T)
        Returns: xs, hs, ys, ps  (for BPTT)
        """
        xs, hs, ys, ps = {}, {}, {}, {}
        hs[-1] = h_prev.copy()
        for t, idx in enumerate(inputs):
            xs[t] = self.vocab.one_hot(idx).reshape(-1, 1)
            hs[t] = tanh(self.W_xh @ xs[t] + self.W_hh @ hs[t-1] + self.b_h)
            ys[t] = self.W_hy @ hs[t] + self.b_y
            ps[t] = softmax(ys[t].ravel())
        return xs, hs, ys, ps

    def backward(self, inputs: list[int], targets: list[int],
                 xs, hs, ps) -> tuple[list, float, np.ndarray]:
        """BPTT. Returns (grads, loss, last_hidden)."""
        dW_xh = np.zeros_like(self.W_xh)
        dW_hh = np.zeros_like(self.W_hh)
        db_h  = np.zeros_like(self.b_h)
        dW_hy = np.zeros_like(self.W_hy)
        db_y  = np.zeros_like(self.b_y)

        loss = 0.0
        dh_next = np.zeros_like(hs[0])
        T = len(inputs)

        for t in reversed(range(T)):
            p = ps[t].copy()
            p[targets[t]] -= 1.0               # ∂cross-entropy / ∂y_t
            dy = p.reshape(-1, 1)
            dW_hy += dy @ hs[t].T
            db_y  += dy
            loss -= math.log(ps[t][targets[t]] + 1e-10)

            dh = self.W_hy.T @ dy + dh_next    # back through output + time
            dtanh = (1 - hs[t] ** 2) * dh      # through tanh
            dW_xh += dtanh @ xs[t].T
            dW_hh += dtanh @ hs[t-1].T
            db_h  += dtanh
            dh_next = self.W_hh.T @ dtanh

        grads = [dW_xh, dW_hh, db_h, dW_hy, db_y]
        # Clip gradients
        for g in grads:
            np.clip(g, -5, 5, out=g)
        return grads, loss / T, hs[T - 1]

    def adam_update(self, grads: list, lr: float = 1e-3,
                    beta1=0.9, beta2=0.999, eps=1e-8):
        self.t += 1
        for i, (p, g, m, v) in enumerate(
                zip(self.params, grads, self.m, self.v)):
            m_new = beta1 * m + (1 - beta1) * g
            v_new = beta2 * v + (1 - beta2) * g ** 2
            self.m[i] = m_new
            self.v[i] = v_new
            m_hat = m_new / (1 - beta1 ** self.t)
            v_hat = v_new / (1 - beta2 ** self.t)
            p -= lr * m_hat / (np.sqrt(v_hat) + eps)

    def train(self, names: list[str], epochs: int = 30, lr: float = 1e-3):
        losses = []
        encoded = [self.vocab.encode(n) for n in names]

        print(f"\n  [{self.name}] params={self.num_params():,}, epochs={epochs}")
        for epoch in range(epochs):
            random.shuffle(encoded)
            epoch_loss = 0.0
            h = np.zeros((self.hidden_size, 1))
            for seq in encoded:
                if len(seq) < 2:
                    continue
                inputs  = seq[:-1]
                targets = seq[1:]
                xs, hs, ys, ps = self.forward(inputs, h)
                grads, loss, h = self.backward(inputs, targets, xs, hs, ps)
                h = np.zeros((self.hidden_size, 1))  # reset hidden per name
                self.adam_update(grads, lr)
                epoch_loss += loss
            avg = epoch_loss / len(encoded)
            losses.append(avg)
            if (epoch + 1) % 5 == 0 or epoch == 0:
                print(f"    Epoch {epoch+1:3d}/{epochs}  loss={avg:.4f}")
        return losses

    def generate(self, max_len: int = 15, temperature: float = 1.0) -> str:
        """Sample a name character by character."""
        h = np.zeros((self.hidden_size, 1))
        idx = self.vocab.start_idx
        name = ""
        for _ in range(max_len):
            x = self.vocab.one_hot(idx).reshape(-1, 1)
            h = tanh(self.W_xh @ x + self.W_hh @ h + self.b_h)
            y = self.W_hy @ h + self.b_y
            p = softmax(y.ravel() / temperature)
            idx = np.random.choice(self.vocab.size, p=p)
            if idx == self.vocab.end_idx:
                break
            name += self.vocab.idx2char[idx]
        return name



#  MODEL 2 – BIDIRECTIONAL LSTM (BLSTM)


class LSTMCell:
    """Single-direction LSTM cell."""

    def __init__(self, input_size: int, hidden_size: int):
        H, I = hidden_size, input_size
        scale = np.sqrt(2 / (I + H))
        # Combined gate weight matrix [i, f, g, o] all in one
        self.W = np.random.randn(4 * H, I + H) * scale
        self.b = np.zeros((4 * H, 1))
        self.H = H

    @property
    def params(self):
        return [self.W, self.b]

    def step(self, x, h_prev, c_prev):
        """One LSTM step. Returns h_next, c_next, cache."""
        combined = np.vstack([x, h_prev])           # (I+H, 1)
        gates = self.W @ combined + self.b          # (4H, 1)
        H = self.H
        i_gate = sigmoid(gates[:H])
        f_gate = sigmoid(gates[H:2*H])
        g_gate = tanh(gates[2*H:3*H])
        o_gate = sigmoid(gates[3*H:4*H])
        c_next = f_gate * c_prev + i_gate * g_gate
        h_next = o_gate * tanh(c_next)
        cache = (x, h_prev, c_prev, i_gate, f_gate, g_gate, o_gate, c_next)
        return h_next, c_next, cache

    def backward(self, dh_next, dc_next, cache):
        x, h_prev, c_prev, i, f, g, o, c_next = cache
        combined = np.vstack([x, h_prev])
        tanh_c = tanh(c_next)

        do = dh_next * tanh_c
        dc = dh_next * o * (1 - tanh_c ** 2) + dc_next
        df = dc * c_prev
        di = dc * g
        dg = dc * i
        dc_prev = dc * f

        dGates = np.vstack([
            di * i * (1 - i),
            df * f * (1 - f),
            dg * (1 - g ** 2),
            do * o * (1 - o),
        ])
        dW = dGates @ combined.T
        db = dGates
        dx_h = self.W.T @ dGates
        dx = dx_h[:x.shape[0]]
        dh_prev = dx_h[x.shape[0]:]
        return dx, dh_prev, dc_prev, dW, db


class BLSTMModel:
    """
    Bidirectional LSTM:
      - Forward LSTM  processes sequence left → right
      - Backward LSTM processes sequence right → left
      - Outputs are concatenated: h_concat = [h_fwd; h_bwd]
      - For generation only the forward direction is used
        (bidirectional only meaningful for encoding tasks,
         but both directions are trained on the full sequence
         to show architectural difference)

    Architecture:
      Input  → Forward LSTM (H) + Backward LSTM (H)  →  [2H] concat
             → Linear (V) → Softmax
    """

    def __init__(self, vocab: CharVocab, hidden_size: int = 64):
        self.vocab = vocab
        self.H = hidden_size
        V = vocab.size

        self.fwd = LSTMCell(V, hidden_size)
        self.bwd = LSTMCell(V, hidden_size)
        # Output layer takes concatenated hidden state
        self.W_out = np.random.randn(V, 2 * hidden_size) * np.sqrt(2 / (2 * hidden_size + V))
        self.b_out = np.zeros((V, 1))

        all_p = self.fwd.params + self.bwd.params + [self.W_out, self.b_out]
        self.all_params = all_p
        self.m = [np.zeros_like(p) for p in all_p]
        self.v = [np.zeros_like(p) for p in all_p]
        self.t = 0

    @property
    def name(self):
        return "BLSTM"

    def num_params(self) -> int:
        return sum(p.size for p in self.all_params)

    def _encode_sequence(self, seq_indices: list[int]):
        """Run both LSTM directions and return per-step hidden states."""
        T = len(seq_indices)
        xvecs = [self.vocab.one_hot(i).reshape(-1, 1) for i in seq_indices]

        # Forward pass
        h_f = np.zeros((self.H, 1))
        c_f = np.zeros((self.H, 1))
        fwd_hs, fwd_cs, fwd_caches = [], [], []
        for x in xvecs:
            h_f, c_f, cache = self.fwd.step(x, h_f, c_f)
            fwd_hs.append(h_f)
            fwd_cs.append(c_f)
            fwd_caches.append(cache)

        # Backward pass
        h_b = np.zeros((self.H, 1))
        c_b = np.zeros((self.H, 1))
        bwd_hs, bwd_cs, bwd_caches = [], [], []
        for x in reversed(xvecs):
            h_b, c_b, cache = self.bwd.step(x, h_b, c_b)
            bwd_hs.insert(0, h_b)
            bwd_cs.insert(0, c_b)
            bwd_caches.insert(0, cache)

        return xvecs, fwd_hs, fwd_caches, bwd_hs, bwd_caches

    def train_step(self, seq: list[int]):
        if len(seq) < 2:
            return 0.0, []
        inputs  = seq[:-1]
        targets = seq[1:]
        xvecs, fwd_hs, fwd_caches, bwd_hs, bwd_caches = self._encode_sequence(inputs)

        T = len(inputs)
        loss = 0.0
        dW_out = np.zeros_like(self.W_out)
        db_out = np.zeros_like(self.b_out)
        dh_fwd = [np.zeros((self.H, 1))] * T
        dh_bwd = [np.zeros((self.H, 1))] * T
        dc_fwd = [np.zeros((self.H, 1))] * T
        dc_bwd = [np.zeros((self.H, 1))] * T

        # Compute output loss
        for t in range(T):
            h_concat = np.vstack([fwd_hs[t], bwd_hs[t]])   # (2H, 1)
            logits = self.W_out @ h_concat + self.b_out
            p = softmax(logits.ravel())
            loss -= math.log(p[targets[t]] + 1e-10)
            dy = p.copy().reshape(-1, 1)
            dy[targets[t]] -= 1.0
            dW_out += dy @ h_concat.T
            db_out += dy
            dh_concat = self.W_out.T @ dy
            dh_fwd[t] = dh_concat[:self.H]
            dh_bwd[t] = dh_concat[self.H:]

        # Backward through forward LSTM
        dW_fwd = np.zeros_like(self.fwd.W)
        db_fwd_g = np.zeros_like(self.fwd.b)
        dh_next_f = np.zeros((self.H, 1))
        dc_next_f = np.zeros((self.H, 1))
        for t in reversed(range(T)):
            dh_t = dh_fwd[t] + dh_next_f
            dc_t = dc_next_f
            dx, dh_next_f, dc_next_f, dW_t, db_t = \
                self.fwd.backward(dh_t, dc_t, fwd_caches[t])
            dW_fwd += dW_t
            db_fwd_g += db_t

        # Backward through backward LSTM
        dW_bwd = np.zeros_like(self.bwd.W)
        db_bwd_g = np.zeros_like(self.bwd.b)
        dh_next_b = np.zeros((self.H, 1))
        dc_next_b = np.zeros((self.H, 1))
        for t in range(T):          # reversed order = forward for bwd LSTM
            dh_t = dh_bwd[t] + dh_next_b
            dc_t = dc_next_b
            dx, dh_next_b, dc_next_b, dW_t, db_t = \
                self.bwd.backward(dh_t, dc_t, bwd_caches[t])
            dW_bwd += dW_t
            db_bwd_g += db_t

        grads = [dW_fwd, db_fwd_g, dW_bwd, db_bwd_g, dW_out, db_out]
        for g in grads:
            np.clip(g, -5, 5, out=g)
        return loss / T, grads

    def adam_update(self, grads, lr=1e-3, b1=0.9, b2=0.999, eps=1e-8):
        self.t += 1
        for i, (p, g, m, v) in enumerate(
                zip(self.all_params, grads, self.m, self.v)):
            mn = b1 * m + (1 - b1) * g
            vn = b2 * v + (1 - b2) * g ** 2
            self.m[i] = mn; self.v[i] = vn
            mh = mn / (1 - b1 ** self.t)
            vh = vn / (1 - b2 ** self.t)
            p -= lr * mh / (np.sqrt(vh) + eps)

    def train(self, names: list[str], epochs: int = 30, lr: float = 1e-3):
        losses = []
        encoded = [self.vocab.encode(n) for n in names]
        print(f"\n  [{self.name}] params={self.num_params():,}, epochs={epochs}")
        for epoch in range(epochs):
            random.shuffle(encoded)
            epoch_loss = 0.0
            for seq in encoded:
                loss, grads = self.train_step(seq)
                if grads:
                    self.adam_update(grads, lr)
                epoch_loss += loss
            avg = epoch_loss / len(encoded)
            losses.append(avg)
            if (epoch + 1) % 5 == 0 or epoch == 0:
                print(f"    Epoch {epoch+1:3d}/{epochs}  loss={avg:.4f}")
        return losses

    def generate(self, max_len: int = 15, temperature: float = 1.0) -> str:
        """Generate using only the forward LSTM (autoregressive)."""
        h = np.zeros((self.H, 1))
        c = np.zeros((self.H, 1))
        idx = self.vocab.start_idx
        name = ""
        for _ in range(max_len):
            x = self.vocab.one_hot(idx).reshape(-1, 1)
            h, c, _ = self.fwd.step(x, h, c)
            # For generation concatenate fwd hidden with zeros for bwd
            h_concat = np.vstack([h, np.zeros((self.H, 1))])
            logits = self.W_out @ h_concat + self.b_out
            p = softmax(logits.ravel() / temperature)
            idx = np.random.choice(self.vocab.size, p=p)
            if idx == self.vocab.end_idx:
                break
            name += self.vocab.idx2char[idx]
        return name



#  MODEL 3 – RNN WITH BASIC ATTENTION


class AttentionRNN:
    """
    RNN with Additive Attention (Bahdanau-style, simplified):
      1. Encoder: run vanilla RNN over a fixed context window of past hidden
         states to build an encoder memory.
      2. Attention: compute alignment scores between current query and memory.
      3. Context vector: weighted sum of memory.
      4. Decoder step: h_t = tanh(W_xh·x_t + W_hh·[h_{t-1}; context] + b)

    Architecture:
      h_t = tanh( W_xh·x_t  +  W_hh·concat(h_{t-1}, context_t)  +  b_h )
      context_t = Σ α_i · memory_i
      α = softmax( v^T · tanh(W_a·memory + U_a·h_{t-1}) )
      y_t = W_hy·h_t + b_y
    """

    MEMORY_LEN = 8   # how many past hidden states to attend over

    def __init__(self, vocab: CharVocab, hidden_size: int = 128):
        self.vocab = vocab
        self.H = hidden_size
        V = vocab.size
        M = self.MEMORY_LEN

        scale = lambda a, b: np.sqrt(2 / (a + b))
        self.W_xh = np.random.randn(hidden_size, V) * scale(V, hidden_size)
        self.W_hh = np.random.randn(hidden_size, hidden_size + hidden_size) \
                    * scale(2 * hidden_size, hidden_size)
        self.b_h  = np.zeros((hidden_size, 1))
        self.W_hy = np.random.randn(V, hidden_size) * scale(hidden_size, V)
        self.b_y  = np.zeros((V, 1))

        # Attention parameters
        self.W_a  = np.random.randn(hidden_size, hidden_size) * 0.01
        self.U_a  = np.random.randn(hidden_size, hidden_size) * 0.01
        self.v_a  = np.random.randn(hidden_size, 1) * 0.01

        self.params = [self.W_xh, self.W_hh, self.b_h,
                       self.W_hy, self.b_y,
                       self.W_a, self.U_a, self.v_a]
        self.m = [np.zeros_like(p) for p in self.params]
        self.v_opt = [np.zeros_like(p) for p in self.params]
        self.t = 0

    @property
    def name(self):
        return "AttentionRNN"

    def num_params(self) -> int:
        return sum(p.size for p in self.params)

    def _attention(self, memory: np.ndarray, h_prev: np.ndarray):
        """
        memory : (M, H) – past hidden states
        h_prev : (H, 1)
        Returns context (H, 1) and attention weights (M,)
        """
        M = memory.shape[0]
        # Score: v^T · tanh(W_a · memory^T + U_a · h_prev)
        # memory^T : (H, M)
        energy = tanh(self.W_a @ memory.T + self.U_a @ h_prev)  # (H, M)
        scores = (self.v_a.T @ energy).ravel()                   # (M,)
        alpha = softmax(scores)                                   # (M,)
        context = memory.T @ alpha                               # (H,)
        return context.reshape(-1, 1), alpha

    def forward(self, inputs: list[int], h_prev: np.ndarray):
        xs, hs, ps = {}, {}, {}
        contexts, alphas = {}, {}
        memory = np.zeros((self.MEMORY_LEN, self.H))
        hs[-1] = h_prev.copy()

        for t, idx in enumerate(inputs):
            xs[t] = self.vocab.one_hot(idx).reshape(-1, 1)
            # Update memory (circular buffer)
            mem = np.roll(memory, -1, axis=0)
            mem[-1] = hs[t-1].ravel()
            memory = mem

            context, alpha = self._attention(memory, hs[t-1])
            contexts[t] = context
            alphas[t] = alpha

            combined = np.vstack([hs[t-1], context])             # (2H, 1)
            hs[t] = tanh(self.W_xh @ xs[t] + self.W_hh @ combined + self.b_h)
            ys_t  = self.W_hy @ hs[t] + self.b_y
            ps[t] = softmax(ys_t.ravel())

        return xs, hs, ps, contexts, alphas, memory

    def train_step(self, seq: list[int]):
        if len(seq) < 2:
            return 0.0, []
        inputs  = seq[:-1]
        targets = seq[1:]
        h0 = np.zeros((self.H, 1))
        xs, hs, ps, contexts, alphas, _ = self.forward(inputs, h0)

        T = len(inputs)
        loss = 0.0
        dW_xh = np.zeros_like(self.W_xh)
        dW_hh = np.zeros_like(self.W_hh)
        db_h  = np.zeros_like(self.b_h)
        dW_hy = np.zeros_like(self.W_hy)
        db_y  = np.zeros_like(self.b_y)
        dW_a  = np.zeros_like(self.W_a)
        dU_a  = np.zeros_like(self.U_a)
        dv_a  = np.zeros_like(self.v_a)
        dh_next = np.zeros_like(hs[0])

        for t in reversed(range(T)):
            p = ps[t].copy()
            p[targets[t]] -= 1.0
            loss -= math.log(ps[t][targets[t]] + 1e-10)
            dy = p.reshape(-1, 1)
            dW_hy += dy @ hs[t].T
            db_y  += dy

            dh = self.W_hy.T @ dy + dh_next
            dtanh = (1 - hs[t] ** 2) * dh

            dW_xh += dtanh @ xs[t].T
            combined = np.vstack([hs[t-1], contexts[t]])
            dW_hh += dtanh @ combined.T
            db_h  += dtanh
            # Gradient to combined (simplified; skip full attention backprop)
            dh_next = self.W_hh.T @ dtanh
            dh_next = dh_next[:self.H]           # only h_{t-1} part

        grads = [dW_xh, dW_hh, db_h, dW_hy, db_y, dW_a, dU_a, dv_a]
        for g in grads:
            np.clip(g, -5, 5, out=g)
        return loss / T, grads

    def adam_update(self, grads, lr=1e-3, b1=0.9, b2=0.999, eps=1e-8):
        self.t += 1
        for i, (p, g, m, v) in enumerate(
                zip(self.params, grads, self.m, self.v_opt)):
            mn = b1 * m + (1 - b1) * g
            vn = b2 * v + (1 - b2) * g ** 2
            self.m[i] = mn; self.v_opt[i] = vn
            mh = mn / (1 - b1 ** self.t)
            vh = vn / (1 - b2 ** self.t)
            p -= lr * mh / (np.sqrt(vh) + eps)

    def train(self, names: list[str], epochs: int = 30, lr: float = 1e-3):
        losses = []
        encoded = [self.vocab.encode(n) for n in names]
        print(f"\n  [{self.name}] params={self.num_params():,}, epochs={epochs}")
        for epoch in range(epochs):
            random.shuffle(encoded)
            epoch_loss = 0.0
            for seq in encoded:
                loss, grads = self.train_step(seq)
                if grads:
                    self.adam_update(grads, lr)
                epoch_loss += loss
            avg = epoch_loss / len(encoded)
            losses.append(avg)
            if (epoch + 1) % 5 == 0 or epoch == 0:
                print(f"    Epoch {epoch+1:3d}/{epochs}  loss={avg:.4f}")
        return losses

    def generate(self, max_len: int = 15, temperature: float = 1.0) -> str:
        h = np.zeros((self.H, 1))
        memory = np.zeros((self.MEMORY_LEN, self.H))
        idx = self.vocab.start_idx
        name = ""
        for _ in range(max_len):
            x = self.vocab.one_hot(idx).reshape(-1, 1)
            mem = np.roll(memory, -1, axis=0)
            mem[-1] = h.ravel()
            memory = mem
            context, _ = self._attention(memory, h)
            combined = np.vstack([h, context])
            h = tanh(self.W_xh @ x + self.W_hh @ combined + self.b_h)
            y = self.W_hy @ h + self.b_y
            p = softmax(y.ravel() / temperature)
            idx = np.random.choice(self.vocab.size, p=p)
            if idx == self.vocab.end_idx:
                break
            name += self.vocab.idx2char[idx]
        return name



#  TASK 2 – QUANTITATIVE EVALUATION


def evaluate(model, training_names: list[str], n: int = 200,
             temperature: float = 0.8) -> dict:
    """
    Generate n names and compute:
      - Novelty Rate: % of generated names NOT in training set
      - Diversity:    # unique names / n
    """
    train_set = set(training_names)
    generated = []
    attempts = 0
    while len(generated) < n and attempts < n * 10:
        name = model.generate(temperature=temperature)
        name = name.strip()
        if len(name) >= 2 and name.isalpha():
            generated.append(name)
        attempts += 1

    if not generated:
        return {"novelty": 0.0, "diversity": 0.0, "samples": []}

    novel = sum(1 for g in generated if g not in train_set)
    unique = len(set(generated))
    return {
        "novelty":   novel / len(generated) * 100,
        "diversity": unique / len(generated) * 100,
        "samples":   generated[:20],
        "total_gen": len(generated),
    }


def print_evaluation_table(results: dict):
    print("\n" + "=" * 65)
    print("  TASK 2 – QUANTITATIVE EVALUATION")
    print("=" * 65)
    print(f"  {'Model':<16} {'Params':>10} {'Novelty%':>10} {'Diversity%':>12}")
    print("  " + "-" * 52)
    for model_name, res in results.items():
        params = res["params"]
        nov    = res["eval"]["novelty"]
        div    = res["eval"]["diversity"]
        print(f"  {model_name:<16} {params:>10,} {nov:>9.1f}% {div:>11.1f}%")



