def generate_names_list() -> list[str]:
    """Parse the names string and return cleaned list of 1000 names."""
    raw = [n.strip() for n in INDIAN_NAMES.split() if n.strip()]
    # Deduplicate while preserving order
    seen = set()
    names = []
    for n in raw:
        nl = n.lower()
        if nl not in seen and n.isalpha():
            seen.add(nl)
            names.append(n.lower())
    # Repeat/extend to reach 1000
    while len(names) < 1000:
        # Add slight variations by common Indian name suffixes/prefixes
        extras = []
        for n in names[:]:
            if len(names) + len(extras) >= 1000:
                break
            extras.append(n + "a" if not n.endswith("a") else n[:-1])
        names.extend(extras[:max(0, 1000 - len(names))])
    return names[:1000]


