# TASK 1 – DATASET PREPARATION


def load_raw_corpus(path):
    with open(path, "r", encoding="utf-8") as f:
        return f.read()

def preprocess(text):
    lines = text.split("\n")
    sentences = []

    for line in lines:
        line = line.encode("ascii", errors="ignore").decode()
        line = line.lower()
        line = re.sub(r"[^a-z\s]", " ", line)
        line = re.sub(r"\s+", " ", line).strip()

        tokens = [w for w in line.split() if len(w) > 2]
        if tokens:
            sentences.append(tokens)

    return sentences

def corpus_statistics(sentences):
    tokens = [w for s in sentences for w in s]
    vocab = set(tokens)
    freq = collections.Counter(tokens)

    print("\n===== DATASET STATS =====")
    print("Documents:", len(sentences))
    print("Tokens:", len(tokens))
    print("Vocab size:", len(vocab))

    print("\nTop words:")
    for w,c in freq.most_common(10):
        print(w, c)

    return freq

