# TASK 3 – SEMANTIC ANALYSIS


def cosine(a,b):
    return np.dot(a,b)/(np.linalg.norm(a)*np.linalg.norm(b)+1e-10)

def nearest(word, model, vocab):
    if word not in vocab.word2idx:
        return []

    idx = vocab.word2idx[word]
    vec = model.W[idx]

    sims = []
    for i in range(vocab.size):
        if i == idx: continue
        sims.append((vocab.idx2word[i], cosine(vec, model.W[i])))

    sims.sort(key=lambda x:-x[1])
    return sims[:5]

def analogy(a,b,c,model,vocab):
    if any(w not in vocab.word2idx for w in [a,b,c]):
        return []

    vec = model.W[vocab.word2idx[b]] - model.W[vocab.word2idx[a]] + model.W[vocab.word2idx[c]]

    sims = []
    for i in range(vocab.size):
        sims.append((vocab.idx2word[i], cosine(vec, model.W[i])))

    sims.sort(key=lambda x:-x[1])
    return sims[:3]

