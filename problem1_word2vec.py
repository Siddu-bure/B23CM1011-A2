# TASK 4 – VISUALIZATION (PCA)


def pca(X):
    X = X - X.mean(axis=0)
    cov = X.T @ X
    eigvals, eigvecs = np.linalg.eigh(cov)
    return X @ eigvecs[:, -2:]

words = list(vocab.word2idx.keys())[:50]
vecs = np.array([model.W[vocab.word2idx[w]] for w in words])

coords = pca(vecs)

plt.figure(figsize=(8,6))
for i, word in enumerate(words):
    plt.scatter(coords[i,0], coords[i,1])
    plt.text(coords[i,0], coords[i,1], word)

