# TASK 2 – WORD2VEC (CBOW + SKIPGRAM + NEGATIVE SAMPLING)


class Vocabulary:
    def __init__(self, sentences):
        freq = collections.Counter(w for s in sentences for w in s)
        self.word2idx = {w:i for i,(w,_) in enumerate(freq.items())}
        self.idx2word = {i:w for w,i in self.word2idx.items()}
        self.size = len(self.word2idx)

    def encode(self, sentences):
        return [[self.word2idx[w] for w in s if w in self.word2idx] for s in sentences]

def sigmoid(x):
    return 1 / (1 + np.exp(-x))

class SkipGram:
    def __init__(self, vocab_size, dim=50, window=2, lr=0.01):
        self.W = np.random.randn(vocab_size, dim)
        self.window = window
        self.lr = lr

    def train(self, data, epochs=3):
        print("\nTraining SkipGram...")
        for ep in range(epochs):
            loss = 0
            for sent in data:
                for i, center in enumerate(sent):
                    context = sent[max(0,i-self.window):i+self.window+1]
                    for word in context:
                        if word == center: continue
                        score = sigmoid(self.W[center] @ self.W[word])
                        grad = score - 1
                        self.W[center] -= self.lr * grad * self.W[word]
                        self.W[word] -= self.lr * grad * self.W[center]
                        loss += -math.log(score+1e-10)
            print(f"Epoch {ep+1} loss {loss:.2f}")

class CBOW:
    def __init__(self, vocab_size, dim=50, window=2, lr=0.01):
        self.W = np.random.randn(vocab_size, dim)
        self.window = window
        self.lr = lr

    def train(self, data, epochs=3):
        print("\nTraining CBOW...")
        for ep in range(epochs):
            loss = 0
            for sent in data:
                for i, target in enumerate(sent):
                    context = sent[max(0,i-self.window):i+self.window+1]
                    context = [w for w in context if w != target]

                    if not context: continue

                    avg = np.mean(self.W[context], axis=0)
                    score = sigmoid(avg @ self.W[target])
                    grad = score - 1

                    for w in context:
                        self.W[w] -= self.lr * grad * self.W[target]

                    self.W[target] -= self.lr * grad * avg
                    loss += -math.log(score+1e-10)

            print(f"Epoch {ep+1} loss {loss:.2f}")

